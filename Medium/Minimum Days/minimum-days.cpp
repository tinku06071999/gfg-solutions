class Solution {
  public:
    int getMinimumDays(int N,string S, vector<int> &P) {
        // code here
        int i=0,j=1,k=0;
        int count=0;
        while(i<N && j<N && k< P.size()){
            if(S[i]==S[j] && (S[i]!='?'|| S[j]!='?')){
                S[P[k++]] = '?';
                count++;
                // cout<<S[i]<<" "<<S[j];
            }
            else{
            i++;
            j++;
                
            }
        }
        // cout<<endl;
        return count;
    }
};


